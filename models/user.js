/////////////////////////////////////////////////////
// Our schema and model for the user resource
/////////////////////////////////////////////////////

/////////////////////////////////////////////////////
// Import Dependences
/////////////////////////////////////////////////////
const mongoose = require('./connection')

const {Schema, model} = mongoose

/////////////////////////////////////////////////////
// Define User Schema
/////////////////////////////////////////////////////
const userSchema = new Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        reuired: true
    }
})

const User = model("User", userSchema)

/////////////////////////////////////////////////////
// Eport our Model
/////////////////////////////////////////////////////
module.exports = User